                                <h2>Cirurgia odontológica</h2>
                                <h3>Cuidados com inflamações, infecções e outros problemas que afetam as gengivas e impactam no restante do organismo, podendo causar outros males fora da boca.</h3>
                                <p>Periodontia é a especialidade que tem como objetivo o estudo dos tecidos de suporte e circundantes dos dentes e seus substitutos, o diagnóstico, a prevenção, o tratamento das alterações nesses tecidos e das manifestações das condições sistêmicas no periodonto, e a terapia de manutenção para o controle da saúde.</p>
                                <p>A doença periodontal é vista como indicador de risco para a ocorrência de doenças cardiovasculares, entre elas o infarto e a endocardite bacteriana.</p>
                                <p>No caso de nascimento prematuro, a periodontite (inflamação das gengivas) é um dos fatores responsáveis pelo evento. </p>
                                <p>Já em indivíduos com diabetes e portadores de periodontite, a situação pode se agravar ainda mais.</p>
                                <p>Trabalhos recentes mostram que o tratamento da periodontite auxilia no controle glicêmico, colaborando para o tratamento da diabetes.</p>
                                <p>Sendo assim, a correta higiene bucal é o principal fator de prevenção de doenças periodontais, uma vez que as bactérias (biofilme) são o agente etiológico principal do processo. </p>
